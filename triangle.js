for (let c = 1; c <=7; c++){ // ++ é a mesma coisa que somar 1, tipo: y = y + 1 === y++
     jv += '#'   // += é a mesma coisa que somar ele mesmo mais alguma coisa, logo: x = x + '#' === x +='#'
    console.log(jv)
}
